// Question 4: Simple Double Average Program
#include <iostream>

using namespace std;

int main()
{
    // Variables
    double num1 = 75.35;
    double num2 = -35.56;
    double num3 = 15.76;
    double average = (num1 + num2 + num3) / 3;

    cout << "The average of " << num1 << ", " << num2 << ", and " << num3 << " is " << average << "." << endl;

    return 0;
}