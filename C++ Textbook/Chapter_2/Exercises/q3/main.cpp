// Question 3: Simple Integer Average Program
#include <iostream>

using namespace std;

int main()
{
    // Variables
    int num1 = 125;
    int num2 = 28;
    int num3 = -25;
    int average = (num1 + num2 + num3) / 3;

    cout << "The average of " << num1 << ", " << num2 << ", and " << num3 << " is " << average << "." << endl;

    return 0;
}