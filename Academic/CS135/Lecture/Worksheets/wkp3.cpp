#include <iostream>

using namespace std;

int main()
{
	// Variables
	const double dbFV = 5.0;
	const double dbTW = 2.0;
	const int intFV = 5;
	const int intTW = 2;

	cout << "int 5 / 2 is " << intFV / intTW << endl;
	cout << "int 5 % 2 is " << intFV % intTW << endl;
	cout << "double 5.0 / 2.0 is " << dbFV / dbTW << endl;

	return 0;
}
